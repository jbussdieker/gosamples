package dns

import "fmt"

////////////////////////////////////////////////////////////////////////////////
// Public functions
////////////////////////////////////////////////////////////////////////////////

func NewQuestion(name string, rtype RecordType, class ClassType) *Question {
	return &Question{
		Name:  name,
		Type:  rtype,
		Class: class,
	}
}

////////////////////////////////////////////////////////////////////////////////
// Method functions
////////////////////////////////////////////////////////////////////////////////

func (q *Question) String() (str string) {
	str += fmt.Sprintf("              Name: %s\n", q.Name)
	str += fmt.Sprintf("              Type: %d\n", q.Type)
	str += fmt.Sprintf("             Class: %d\n", q.Class)
	return
}
